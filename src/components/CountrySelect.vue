<template>
    <select @change="onChange()" v-model="selected" class="form-select bg-white block w-1/2  border p-3 rounded mt-10">
        <option value="0">Select Country</option>
        <option v-for="country in countries" :key="country.ID">
            {{ country.Country }}
        </option>
    </select>
</template>

<script>
export default {
    name: 'CountrySelect',
    props: ['countries'],
    data() {
        return {
            selected: 0
        }
    },
    methods: {
        onChange() {
           const country = this.countries.find((item) => item.Country === this.selected)
           this.$emit('get-country', country)
        }
    }
}
</script>